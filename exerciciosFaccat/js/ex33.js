let n1 = parseInt(prompt("Digite o primero valor:"))
let n2 = parseInt(prompt("Digite o segundo valor:"))

if (n1 = n2){
    alert("Os números digitados são iguais!")
}else{
    if (n1 > n2){
        alert("O primeiro número é o maior!")
    }else{
        alert("O segundo número é o maior!")
    }
}